    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        if (txtFullname.getText().equals("") || txtUsername.getText().equals("") || txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "data tidak boleh kosong", "validasi data", JOptionPane.INFORMATION_MESSAGE);
            captcha();
            return;
        }else {
            if (!txtCaptcha.getText().equals(lbCaptcha.getText().toString())){
                JOptionPane.showMessageDialog(null, "The captcha code does not match.", "Error", JOptionPane.INFORMATION_MESSAGE);
                captcha();
                return;
            }else {
                registerController.register();
                clearField();
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

